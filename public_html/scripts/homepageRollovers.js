function rollover() {
    // changes image when mouse rolls over
    var secondHomepageImage = document.getElementById("secondHomepageImage");
    secondHomepageImage.style.backgroundImage = "url(images/homepage/menswear_rollover.jpg)";
}

function rollout() {
    // changes image back when mouse rolls out
    var secondHomepageImage = document.getElementById("secondHomepageImage");
    secondHomepageImage.style.backgroundImage = "url(images/homepage/menswear_desktop.jpg)";
}
